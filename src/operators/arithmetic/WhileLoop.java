package operators.arithmetic;

public class WhileLoop {
    public static void main (String [] args){

        //using for loop
        for (int i=1; i<=5;i++){
            System.out.println(i);
        }
        System.out.println();


        //using while loop
        int j = 1;
        while (j<=5){
            System.out.println(j);
            j++;
        }
    }
}
